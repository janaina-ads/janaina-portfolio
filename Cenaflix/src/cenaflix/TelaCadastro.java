    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       
       int linha = tblFilmeS.getSelectedRow();

    if (linha == -1) {
        javax.swing.JOptionPane.showMessageDialog(null, "Selecione um filme para excluir!");
        return;
    }

    int id = (int) tblFilmeS.getValueAt(linha, 0);

    int confirmar = javax.swing.JOptionPane.showConfirmDialog(null, 
            "Deseja excluir este filme?", 
            "Confirmação", 
            javax.swing.JOptionPane.YES_NO_OPTION);

    if (confirmar == javax.swing.JOptionPane.YES_OPTION) {

        try {
            java.sql.Connection con = Conexao.conectar();

            String sql = "DELETE FROM filmes WHERE id = ?";
            java.sql.PreparedStatement stmt = con.prepareStatement(sql);

            stmt.setInt(1, id);

            stmt.execute();
            stmt.close();

            javax.swing.JOptionPane.showMessageDialog(null, "Filme excluído com sucesso!");

            // 🔥 Atualiza tabela
            listarFilmes();

            // 🔥 Limpa campos
            txtNome.setText("");
            txtData.setText("");
            txtCategoria.setText("");

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Erro ao excluir!");
        }
    }
    }//GEN-LAST:event_btnExcluirActionPerformed

