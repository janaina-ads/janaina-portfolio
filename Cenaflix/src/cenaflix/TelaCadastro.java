    public void listarFilmes() {
        try {
            java.sql.Connection con = Conexao.conectar();

            String sql = "SELECT * FROM filmes";
            java.sql.PreparedStatement stmt = con.prepareStatement(sql);
            java.sql.ResultSet rs = stmt.executeQuery();

            javax.swing.table.DefaultTableModel modelo =
            (javax.swing.table.DefaultTableModel) tblFilmeS.getModel();

            modelo.setRowCount(0);

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nome"),
                    rs.getString("datalancamento"),
                    rs.getString("categoria")
                });
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Erro ao listar dados!");
        }
    }

    public void buscarPorCategoria() {

        if (txtBusca.getText().isEmpty()) {
            listarFilmes();
            return;
        }

        try {
            java.sql.Connection con = Conexao.conectar();

            String sql = "SELECT * FROM filmes WHERE categoria LIKE ?";
            java.sql.PreparedStatement stmt = con.prepareStatement(sql);

            stmt.setString(1, "%" + txtBusca.getText() + "%");

            java.sql.ResultSet rs = stmt.executeQuery();

            javax.swing.table.DefaultTableModel modelo =
            (javax.swing.table.DefaultTableModel) tblFilmeS.getModel();

            modelo.setRowCount(0);

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nome"),
                    rs.getString("datalancamento"),
                    rs.getString("categoria")
                });
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Erro na busca!");
        }
    }

    
