    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       if (txtNome.getText().isEmpty() || txtData.getText().isEmpty() || txtCategoria.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        return;
    }

    try {
        java.sql.Connection con = Conexao.conectar();

        String sql = "INSERT INTO filmes (nome, datalancamento, categoria) VALUES (?, ?, ?)";
        java.sql.PreparedStatement stmt = con.prepareStatement(sql);

        stmt.setString(1, txtNome.getText());
        stmt.setString(2, txtData.getText());
        stmt.setString(3, txtCategoria.getText());

        stmt.execute();
        stmt.close();

        javax.swing.JOptionPane.showMessageDialog(null, "Filme cadastrado com sucesso!");

        // 🔥 ATUALIZA A TABELA
        listarFilmes();

        // 🔥 LIMPA OS CAMPOS
        txtNome.setText("");
        txtData.setText("");
        txtCategoria.setText("");

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Erro ao cadastrar!");
    }


    }//GEN-LAST:event_btnCadastrarActionPerformed

